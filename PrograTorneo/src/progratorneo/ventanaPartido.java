    private void dibujarCamisa(Graphics g, Image camisa, Image estam) {
        super.paint(g);
        g.drawRect(0, 0, 240, 240);
        g.setColor(white);
        g.fillRect(0, 0, 240, 240);
        g.drawImage(camisa, 0, 0, null);
        if (estam != null) {
            g.drawImage(estam, 0, 0, null);
        }
    }
    
